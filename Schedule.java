
public class Schedule {
	private int start;
	private int end;
	private int number;

	public int getnumber() {
		return number;
	}
	public void setnumber(int number) {
		this.number = number;
	}
	public int getstart() {
		return start;
	}
	public void setstart(int start) {
		this.start = start;
	}
	public int getend() {
		return end;
	}
	public void setend(int end) {
		this.end = end;
	}
}
